public class Variables {
    public static void main(String[] args) {
        int id; // declaration
        id = 2102103; // assignment
        int roll = 103; // initialization
        System.out.println("Your id and roll is: " + id + " and " + roll);

        int x = 123;
        System.out.println("The value of x is: " + x);

        double y = 3.14;
        System.out.println("The value of y is: " + y);

        boolean z = true;
        System.out.println("The value of z is: " + z);

        char symbol = '@';
        System.out.println("The symbol is: " + symbol);

        String name = "Abid";
        System.out.println("Good job " + name);
    }
}
